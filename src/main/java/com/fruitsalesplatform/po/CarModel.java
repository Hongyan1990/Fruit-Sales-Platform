package com.fruitsalesplatform.po;

public class CarModel {
	private int carId;
	private int uId;
	private int state;
	private float total;
	public int getcId() {
		return carId;
	}
	public void setcId(int carId) {
		this.carId = carId;
	}
	public int getuId() {
		return uId;
	}
	public void setuId(int uId) {
		this.uId = uId;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public float getTotal() {
		return total;
	}
	public void setTotal(float total) {
		this.total = total;
	}
	
}
